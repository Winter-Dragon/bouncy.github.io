document.querySelector('.s8__center-item1').onmouseover = function() {
 	document.querySelector(".s8__center-top1").style.fontSize = '16px';
  	document.querySelector(".s8__center-top1").style.fontWeight = '400';
    document.querySelector(".s8__center-top1").style.textTransform = 'none';
    document.getElementById("s8_change1").innerHTML = 'Let’s Start <i class="fas fa-chevron-right margin-left"></i>';
};
document.querySelector('.s8__center-item1').onmouseout = function() {
 	document.querySelector(".s8__center-top1").style.fontSize = '20px';
  	document.querySelector(".s8__center-top1").style.fontWeight = '700';
    document.querySelector(".s8__center-top1").style.textTransform = 'uppercase';
    document.getElementById("s8_change1").innerHTML = 'Premium';
};
